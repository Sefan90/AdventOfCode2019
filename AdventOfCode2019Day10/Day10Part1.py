def FindAsteroids(map,ex,ey):
	counter = 0
	cords = []
	remove = []
	for x in range(-ex, len(map)-ex):
		for y in range(-ey,len(map[ex])-ey):
			if map[x+ex][y+ey] == "#":
				counter += 1
				cords.append([x,y])
	#print(cords)
	for c in cords:
		for cc in cords:
			if (c == [0,0] and c not in remove):
				remove.append(c)
				#print("1 "+ str(c) + " " + str(cc))
			elif (c[0] == cc[0]*2 and c[1] == cc[1]*2 and c not in remove and c != cc):
				remove.append(c)
				#print("2 "+ str(c) + " " + str(cc))
			elif (c[0] == cc[0]*3 and c[1] == cc[1]*3 and c not in remove and c != cc):
				remove.append(c)
				#print("3 "+ str(c) + " " + str(cc))
			elif (c[0] == cc[0]*4 and c[1] == cc[1]*4 and c not in remove and c != cc):
				remove.append(c)
				#print("4 "+ str(c) + " " + str(cc))
	for r in remove:
		cords.remove(r)
	#print(cords)
	#print(len(cords))
	return len(cords)

